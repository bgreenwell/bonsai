/// Benchmark: bonsai vs ONNX Runtime (ort) for XGBoost binary classification.
///
/// Prerequisites — run once before `cargo bench`:
///   cd examples/xgboost_benchmark
///   uv venv && uv pip install -r requirements.txt
///   uv run python generate.py
///   cd ../..
///   cargo run -- examples/xgboost_benchmark/generated/model.json \
///       -o examples/xgboost_benchmark/generated/model.rs
use criterion::{black_box, criterion_group, criterion_main, BenchmarkId, Criterion, Throughput};
use ort::session::Session;
use ort::value::Tensor;
use std::path::Path;

// Include the bonsai-transpiled model. The file is generated by running
// `cargo run -- .../model.json -o .../model.rs` as documented above.
// If the file is absent `cargo bench` will fail with a clear missing-file error.
mod bonsai_model {
    include!("../examples/xgboost_benchmark/generated/model.rs");
}

// 10 representative feature values (same shape as the trained model).
// These are held constant so that timing variance reflects tree-traversal
// cost rather than data-loading cost.
const N_FEATURES: usize = 10;
const SAMPLE: [f32; N_FEATURES] = [
    0.382f32, -1.241, 0.917, 0.053, -0.628, 1.774, -0.391, 0.826, -1.102, 0.317,
];

fn bench_bonsai(c: &mut Criterion) {
    let model = bonsai_model::Model;
    let features: Vec<f32> = SAMPLE.to_vec();

    let mut group = c.benchmark_group("xgboost_binary_classification");

    // Single-row latency — the primary online-serving metric
    group.throughput(Throughput::Elements(1));
    group.bench_function("bonsai/single", |b| {
        b.iter(|| model.predict(black_box(&features)))
    });

    // Batch throughput — loop over N rows (Phase 1 scalar baseline;
    // Phase 2 will replace this with model.predict_batch once implemented)
    for batch_size in [10usize, 100, 1_000] {
        let batch: Vec<f32> = SAMPLE
            .iter()
            .copied()
            .cycle()
            .take(N_FEATURES * batch_size)
            .collect();

        group.throughput(Throughput::Elements(batch_size as u64));
        group.bench_with_input(
            BenchmarkId::new("bonsai/batch", batch_size),
            &batch_size,
            |b, &n| {
                let mut out = vec![0.0f32; n];
                b.iter(|| {
                    model.predict_batch(black_box(&batch), N_FEATURES, black_box(&mut out));
                    black_box(&out);
                })
            },
        );
    }

    group.finish();
}

fn bench_ort(c: &mut Criterion) {
    let onnx_path = Path::new(env!("CARGO_MANIFEST_DIR"))
        .join("examples/xgboost_benchmark/generated/model.onnx");

    if !onnx_path.exists() {
        eprintln!(
            "\n[xgboost bench] ONNX model not found at {}.\n\
             Run `uv run python examples/xgboost_benchmark/generate.py` first.\n\
             Skipping ort benchmarks.",
            onnx_path.display()
        );
        return;
    }

    let mut session = Session::builder()
        .unwrap()
        .commit_from_file(&onnx_path)
        .unwrap();

    let features: Vec<f32> = SAMPLE.to_vec();

    let mut group = c.benchmark_group("xgboost_binary_classification");

    // Single-row latency
    // onnxmltools export: input="float_input", output[0]=labels, output[1]=probabilities (N,2)
    group.throughput(Throughput::Elements(1));
    group.bench_function("ort/single", |b| {
        b.iter(|| {
            let array = ndarray::Array2::from_shape_vec((1, N_FEATURES), features.clone()).unwrap();
            let input = Tensor::from_array(array).unwrap();
            let outputs = session.run(ort::inputs!["float_input" => input]).unwrap();
            let probs = outputs[1].try_extract_tensor::<f32>().unwrap().1;
            black_box(probs[1]) // positive-class probability
        })
    });

    // Batch throughput — single session.run call with N rows
    for batch_size in [10usize, 100, 1_000] {
        let batch: Vec<f32> = SAMPLE
            .iter()
            .copied()
            .cycle()
            .take(N_FEATURES * batch_size)
            .collect();

        group.throughput(Throughput::Elements(batch_size as u64));
        group.bench_with_input(
            BenchmarkId::new("ort/batch", batch_size),
            &batch_size,
            |b, &n| {
                b.iter(|| {
                    let array =
                        ndarray::Array2::from_shape_vec((n, N_FEATURES), batch.clone()).unwrap();
                    let input = Tensor::from_array(array).unwrap();
                    let outputs = session.run(ort::inputs!["float_input" => input]).unwrap();
                    let probs = outputs[1].try_extract_tensor::<f32>().unwrap().1;
                    black_box(probs[1])
                })
            },
        );
    }

    group.finish();
}

criterion_group!(benches, bench_bonsai, bench_ort);
criterion_main!(benches);
